    /**
     * Builds the query builder used by the manual CRUD index pages.
     */
    public function createIndexQueryBuilder(array $metadata, string $search = '', ?string $status = null, ?string $quality = null): QueryBuilder
    {
        $repository = $this->entityManager->getRepository($metadata['class']);
        $queryBuilder = $repository->createQueryBuilder('entity');

        if ('' !== $search) {
            $or = $queryBuilder->expr()->orX();

            foreach ($metadata['search_fields'] as $index => $field) {
                $parameter = sprintf('search_%d', $index);
                $or->add(sprintf('LOWER(entity.%s) LIKE :%s', $field, $parameter));
                $queryBuilder->setParameter($parameter, '%'.mb_strtolower($search).'%');
            }

            $queryBuilder->andWhere($or);
        }

        if ($this->supportsActiveFilter($metadata) && in_array($status, ['active', 'inactive'], true)) {
            $queryBuilder
                ->andWhere(sprintf('entity.%s = :activeStatus', $metadata['active_field']))
                ->setParameter('activeStatus', 'active' === $status)
            ;
        }

        $this->applyQualityFilter($queryBuilder, $metadata, $quality);

        foreach ($metadata['default_order'] as $field => $direction) {
            $queryBuilder->addOrderBy(sprintf('entity.%s', $field), $direction);
        }

        return $queryBuilder;
    }

