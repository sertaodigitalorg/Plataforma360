    /**
     * Automatically generates a slug when the entity supports it and none was provided.
     *
     * @param array<string, mixed> $metadata
     */
    public function fillSlugIfMissing(object $entity, array $metadata): void
    {
        if (!isset($metadata['slug_field']) || null === $metadata['slug_field']) {
            return;
        }

        $currentSlug = $this->readProperty($entity, $metadata['slug_field']);

        if (is_string($currentSlug) && '' !== trim($currentSlug)) {
            return;
        }

        $title = (string) $this->readProperty($entity, $metadata['title_field']);
        $normalized = iconv('UTF-8', 'ASCII//TRANSLIT//IGNORE', $title);
        $normalized = false === $normalized ? $title : $normalized;
        $normalized = preg_replace('/[^a-z0-9]+/i', '-', strtolower(trim($normalized))) ?? '';

        $this->writeProperty($entity, $metadata['slug_field'], trim($normalized, '-'));
    }

    /**
