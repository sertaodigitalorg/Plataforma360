    private function applyQualityFilter(QueryBuilder $queryBuilder, array $metadata, ?string $quality): void
    {
        if (null === $quality || '' === $quality) {
            return;
        }

        if ('inactive' === $quality && $this->supportsActiveFilter($metadata)) {
            $queryBuilder
                ->andWhere(sprintf('entity.%s = :qualityInactive', $metadata['active_field']))
                ->setParameter('qualityInactive', false)
            ;

            return;
        }

        if ('no_geo' === $quality && isset($metadata['geo_fields']) && null !== $metadata['geo_fields']) {
            [$latitudeField, $longitudeField] = $metadata['geo_fields'];
            $queryBuilder->andWhere(sprintf('entity.%s IS NULL OR entity.%s IS NULL', $latitudeField, $longitudeField));

            return;
        }

        if ('no_description' === $quality && isset($metadata['description_field']) && null !== $metadata['description_field']) {
            $descriptionField = $metadata['description_field'];
            $queryBuilder->andWhere(sprintf('entity.%s IS NULL OR entity.%s = :emptyDescription', $descriptionField, $descriptionField));
            $queryBuilder->setParameter('emptyDescription', '');

            return;
        }

        if ('no_source' === $quality && isset($metadata['source_field']) && null !== $metadata['source_field']) {
            $sourceField = $metadata['source_field'];
            $queryBuilder->andWhere(sprintf('entity.%s IS NULL OR entity.%s = :emptySource', $sourceField, $sourceField));
            $queryBuilder->setParameter('emptySource', '');

            return;
        }

        if ('incomplete' !== $quality) {
            return;
        }

        $conditions = [];

        if (isset($metadata['geo_fields']) && null !== $metadata['geo_fields']) {
            [$latitudeField, $longitudeField] = $metadata['geo_fields'];
            $conditions[] = sprintf('entity.%s IS NULL OR entity.%s IS NULL', $latitudeField, $longitudeField);
        }

        if (isset($metadata['description_field']) && null !== $metadata['description_field']) {
            $conditions[] = sprintf('entity.%s IS NULL OR entity.%s = :emptyIncompleteDescription', $metadata['description_field'], $metadata['description_field']);
            $queryBuilder->setParameter('emptyIncompleteDescription', '');
        }

        if (isset($metadata['source_field']) && null !== $metadata['source_field']) {
            $conditions[] = sprintf('entity.%s IS NULL OR entity.%s = :emptyIncompleteSource', $metadata['source_field'], $metadata['source_field']);
            $queryBuilder->setParameter('emptyIncompleteSource', '');
        }

        if (isset($metadata['review_fields']) && is_array($metadata['review_fields'])) {
            foreach ($metadata['review_fields'] as $index => $field) {
                $parameter = sprintf('emptyReviewField_%d', $index);
                $conditions[] = sprintf('entity.%s IS NULL OR entity.%s = :%s', $field, $field, $parameter);
                $queryBuilder->setParameter($parameter, '');
            }
        }

        if ([] === $conditions) {
            return;
        }

        $queryBuilder->andWhere('('.implode(') OR (', $conditions).')');
    }

    /**
     * Determines whether the record is missing geolocation data.
     *
     * @param array<string, mixed> $metadata
     */
    private function hasMissingGeo(object $entity, array $metadata): bool
    {
        if (!isset($metadata['geo_fields']) || null === $metadata['geo_fields']) {
            return false;
        }

        [$latitudeField, $longitudeField] = $metadata['geo_fields'];

        return $this->isEmpty($this->readProperty($entity, $latitudeField)) || $this->isEmpty($this->readProperty($entity, $longitudeField));
    }

    /**
     * Determines whether the record is missing source data.
     *
     * @param array<string, mixed> $metadata
     */
    private function hasMissingSource(object $entity, array $metadata): bool
    {
        if (!isset($metadata['source_field']) || null === $metadata['source_field']) {
            return false;
        }

        return $this->isEmpty($this->readProperty($entity, $metadata['source_field']));
    }

    /**
     * Determines whether the record needs editorial review.
     *
     * @param array<string, mixed> $metadata
     */
    private function needsReview(object $entity, array $metadata): bool
    {
        if (isset($metadata['description_field']) && null !== $metadata['description_field']) {
            if ($this->isEmpty($this->readProperty($entity, $metadata['description_field']))) {
                return true;
            }
        }

        if (isset($metadata['review_fields']) && is_array($metadata['review_fields'])) {
            foreach ($metadata['review_fields'] as $field) {
                if ($this->isEmpty($this->readProperty($entity, $field))) {
                    return true;
                }
            }
        }

        if ($this->supportsActiveFilter($metadata)) {
            return true !== $this->readProperty($entity, $metadata['active_field']);
        }

        return false;
    }

    /**
     * Safely reads a public property, getter or nested property path.
     */
    private function readProperty(object $entity, string $propertyPath): mixed
    {
        return $this->propertyAccessor->getValue($entity, $propertyPath);
    }

    /**
     * Safely writes a public property or setter-backed property.
     */
    private function writeProperty(object $entity, string $propertyPath, mixed $value): void
    {
        $this->propertyAccessor->setValue($entity, $propertyPath, $value);
    }

    /**
     * Formats values for detail views and summaries.
     */
    private function formatValue(mixed $value): string
    {
        if (null === $value) {
            return 'Não informado';
        }

        if (is_bool($value)) {
            return $value ? 'Sim' : 'Não';
        }

        if ($value instanceof \DateTimeInterface) {
            return $value->format('d/m/Y H:i');
        }

        if (is_array($value)) {
            return [] === $value ? 'Não informado' : implode(', ', $value);
        }

        if (is_object($value)) {
            foreach (['name', 'title'] as $property) {
                if ($this->propertyAccessor->isReadable($value, $property)) {
                    $resolved = $this->propertyAccessor->getValue($value, $property);

                    if (is_string($resolved) && '' !== trim($resolved)) {
                        return $resolved;
                    }
                }
            }

            return 'Registro relacionado';
        }

        $stringValue = trim((string) $value);

        return '' === $stringValue ? 'Não informado' : $stringValue;
    }

    /**
     * Determines whether a scalar value should be considered empty for hygiene rules.
     */
    private function isEmpty(mixed $value): bool
    {
        if (null === $value) {
            return true;
        }

        if (is_string($value)) {
            return '' === trim($value);
        }

        if (is_array($value)) {
            return [] === $value;
        }

        return false;
    }

    /**
     * Truncates long strings while preserving a readable summary.
     */
    private function truncate(string $value, int $limit): string
    {
        $normalized = trim(preg_replace('/\s+/', ' ', strip_tags($value)) ?? '');

        if (mb_strlen($normalized) <= $limit) {
            return $normalized;
        }

        return rtrim(mb_substr($normalized, 0, $limit - 1)).'…';
    }
