    #[Route('/ingestion', name: 'app_admin_ingestion_runs', methods: ['GET'])]
    public function ingestion(IngestionRunRepository $runRepository, DataProviderRepository $providerRepository): Response
    {
        $runs = $runRepository->findRecentRuns(30);
        $providers = $providerRepository->findOrderedForDashboard();

        $statusCounts = array_fill_keys(IngestionRun::getAvailableStatuses(), 0);
        foreach ($runs as $run) {
            $statusCounts[$run->getStatus()] = ($statusCounts[$run->getStatus()] ?? 0) + 1;
        }

        return $this->render('admin/data_providers/ingestion.html.twig', [
            'runs' => $runs,
            'providers' => $providers,
            'statusPresentation' => $this->getRunStatusPresentation(),
            'summary' => [
                'totalRuns' => count($runs),
                'running' => $statusCounts[IngestionRun::STATUS_RUNNING] ?? 0,
                'success' => $statusCounts[IngestionRun::STATUS_SUCCESS] ?? 0,
                'failed' => $statusCounts[IngestionRun::STATUS_FAILED] ?? 0,
            ],
        ]);
    }

    #[Route('/history', name: 'app_admin_ingestion_history', methods: ['GET'])]
    public function history(IngestionRunRepository $runRepository): Response
    {
        return $this->render('admin/data_providers/history.html.twig', [
            'runs' => $runRepository->findRecentRuns(50),
            'statusPresentation' => $this->getRunStatusPresentation(),
        ]);
    }

    #[Route('/{id<\d+>}/sync', name: 'app_admin_data_providers_sync', methods: ['POST'])]
    public function syncPackages(DataProvider $provider, CkanProviderService $ckanProviderService): Response
    {
        try {
            $result = $ckanProviderService->syncPackageList($provider);
            $this->addFlash('success', $result['message']);
        } catch (\Throwable $exception) {
            $this->addFlash('danger', $exception->getMessage());
        }

        return $this->redirectToRoute('app_admin_data_providers', [], Response::HTTP_SEE_OTHER);
    }

    #[Route('/{id<\d+>}/packages', name: 'app_admin_data_providers_packages', methods: ['GET'])]
    public function providerPackages(DataProvider $provider, ProviderPackageRepository $packageRepository): Response
    {
        return $this->render('admin/data_providers/packages.html.twig', [
            'packages' => $packageRepository->findByProviderOrdered($provider),
            'provider' => $provider,
        ]);
    }

    #[Route('/packages/{id<\d+>}', name: 'app_admin_provider_package_show', methods: ['GET'])]
    public function showPackage(ProviderPackage $package, CkanProviderService $ckanProviderService): Response
    {
        $metadata = $package->getRawMetadata();

        try {
            $result = $ckanProviderService->syncPackageResources($package);
            $metadata = $result['metadata'];
            $this->addFlash('success', $result['message']);
        } catch (\Throwable $exception) {
            $this->addFlash('warning', sprintf('Não foi possível atualizar os detalhes em tempo real. Exibindo os dados salvos. %s', $exception->getMessage()));
        }

        return $this->render('admin/data_providers/package_show.html.twig', [
            'package' => $package,
            'metadata' => $metadata,
            'resources' => $package->getResources(),
        ]);
    }

