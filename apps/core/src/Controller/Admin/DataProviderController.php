    #[Route('/new', name: 'app_admin_data_providers_new', methods: ['GET', 'POST'])]
    public function new(Request $request, EntityManagerInterface $entityManager): Response
    {
        $provider = (new DataProvider())
            ->setType(DataProvider::TYPE_CKAN)
            ->setBaseUrl('https://dados.turismo.gov.br')
            ->setPackageListPath('/api/3/action/package_list')
            ->setPackageShowPath('/api/3/action/package_show?id={package_id}')
            ->setIsActive(true)
        ;

        $form = $this->createForm(DataProviderType::class, $provider);
        $form->handleRequest($request);

        if ($form->isSubmitted() && $form->isValid()) {
            $entityManager->persist($provider);
            $entityManager->flush();

            $this->addFlash('success', 'Provedor de dados salvo com sucesso.');

            return $this->redirectToRoute('app_admin_data_providers', [], Response::HTTP_SEE_OTHER);
        }

        return $this->render('admin/data_providers/form.html.twig', [
            'provider' => $provider,
            'form' => $form,
            'isEdit' => false,
        ]);
    }

    #[Route('/{id<\d+>}/edit', name: 'app_admin_data_providers_edit', methods: ['GET', 'POST'])]
    public function edit(Request $request, DataProvider $provider, EntityManagerInterface $entityManager): Response
    {
        $form = $this->createForm(DataProviderType::class, $provider);
        $form->handleRequest($request);

        if ($form->isSubmitted() && $form->isValid()) {
            $entityManager->flush();
